    private void getStudentInfo(String receiveStudentId) {
        try {
            // Connect to the database
            String user = "root";
            String password = "";
            String url = "jdbc:mysql://localhost:3306/studentmangementsystem?serverTimezone=UTC";
            connection = DriverManager.getConnection(url, user, password);

            //Select Name
            String queryForRetriveStdInfo = "SELECT * FROM stdinfo WHERE stdId = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(queryForRetriveStdInfo);
            preparedStatement.setString(1, receiveStudentId);

            ResultSet resultSet = preparedStatement.executeQuery();
            if (resultSet.next()) {
                stdProfileNamePass = resultSet.getString("stdName");
                studenetProfileIdPass = resultSet.getString("stdId");
                studenetProfileName.setText(resultSet.getString("stdName"));
                studenetProfileId.setText(resultSet.getString("stdId"));
                studenetProfileProgram.setText(resultSet.getString("stdDept"));
                studenetProfileSemester.setText(resultSet.getString("stdSemester"));
                studenetProfilePhoneNumber.setText(resultSet.getString("stdPhone"));
                studenetProfileYear.setText(resultSet.getString("stdYear"));
                studenetProfileIntake.setText(resultSet.getString("stdIntake"));
                studentProfileBirthDate.setText(resultSet.getString("stdBirth"));
                studenetProfileRegCourse.setText(StudentHome.count);
            }

            resultSet.close();
            preparedStatement.close();

            String comCourseCount = "SELECT COUNT(stdId) FROM result WHERE stdId = ?";
            preparedStatement = connection.prepareStatement(comCourseCount);
            preparedStatement.setString(1, receiveStudentId);
            resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                String comCourse = Integer.toString(resultSet.getInt(1));
                studenetProfileComCourse.setText(comCourse);

            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        
        String getResult = resultCalculator.CGPA(receiveStudentId);
        
        if(getResult != null && !(getResult.equals(""))){
            studenetProfileCgpa.setText(getResult);
        }else{
             studenetProfileCgpa.setText("00");
        }
        
        
    }

