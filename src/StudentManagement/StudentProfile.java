    private void resetPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetPasswordActionPerformed
        // TODO add your handling code here:
        String usercurrentPassword = currentPassword.getText();
        String userNewPass = newPassword.getText();

        if (usercurrentPassword.equals("") || usercurrentPassword.equals("   Enter your current password")) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Your current Password");
        } else if (userNewPass.equals("") || userNewPass.equals("   Enter your new password")) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Your new Password");
        } else {
            try {

                String resetPassStdId = MainPage.passStudentId;
                String currentPasswordInput = currentPassword.getText();
                String newPasswordInput = newPassword.getText();

                if (currentPasswordInput.equals("") || currentPasswordInput.equals("   Enter your current password")) {
                    JOptionPane.showMessageDialog(rootPane, "Current password field required");
                } else if (newPasswordInput.equals("") || newPasswordInput.equals("   Enter your current password")) {
                    JOptionPane.showMessageDialog(rootPane, "New password field required");
                } else {
                    // Connect to the database
                    String user = "root";
                    String password = "";
                    String url = "jdbc:mysql://localhost:3306/studentmangementsystem?serverTimezone=UTC";
                    connection = DriverManager.getConnection(url, user, password);
                    String queryForRetrivePass = "SELECT stdPasswor FROM stdinfo WHERE stdId = ?";
                    PreparedStatement preparedStatement = connection.prepareStatement(queryForRetrivePass);
                    preparedStatement.setString(1, resetPassStdId);
                    
                    ResultSet resultSet = preparedStatement.executeQuery();
                    
                    if(resultSet.next()){
                        if(resultSet.getString("stdPasswor").equals(currentPasswordInput)){
                            
                             String queryForResetPass = "UPDATE stdinfo SET stdPasswor = ? WHERE stdId = ?";
                             PreparedStatement statementForResetPass = connection.prepareStatement(queryForResetPass);
                             statementForResetPass.setString(1, newPasswordInput);
                             statementForResetPass.setString(2, resetPassStdId);
                             
                             int effect = statementForResetPass.executeUpdate();
                             
                             if(effect > 0){
                                  JOptionPane.showMessageDialog(rootPane, "Sucessfully reset password");
                             }else{
                                JOptionPane.showMessageDialog(rootPane, "Faild to reset password");
                             }
                            statementForResetPass.close();
                             
                        }else{
                            JOptionPane.showMessageDialog(rootPane, "Incorrect current password");
                        }
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "Faild to reset password");
                    }
                    
                    resultSet.close();
                    preparedStatement.close();

                }
                

            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

        newPassword.setText("   Enter your new password");
        newPassword.setForeground(Color.gray);
        newPassword.setEchoChar('\u0000');
        currentPassword.setText("   Enter your current password");
        currentPassword.setForeground(Color.gray);
        currentPassword.setEchoChar('\u0000');
    }//GEN-LAST:event_resetPasswordActionPerformed

