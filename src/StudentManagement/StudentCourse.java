    private void getStudentInfo(String receiveStdId) {
        studenetProfileNameCourse.setText(StudentHome.studentNamsPass);
        studenetProfileIdCourse.setText(receiveStdId);
        try {
            // Connect to the database
            String user = "root";
            String password = "";
            String url = "jdbc:mysql://localhost:3306/studentmangementsystem?serverTimezone=UTC";
            connection = DriverManager.getConnection(url, user, password);

            //Select Name
            String qureyForSelectRegSub = "SELECT intake.courseTitle, intake.courseCode, courses.credit "
                    + "FROM intake "
                    + "INNER JOIN courses "
                    + "ON intake.courseCode = courses.courseCode "
                    + "WHERE stdId = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(qureyForSelectRegSub);
            preparedStatement.setString(1, receiveStdId);

            ResultSet resultSet = preparedStatement.executeQuery();

            int index = 1;

            while (resultSet.next()) {
                if (index == 1) {
                    courseNameOne.setText(resultSet.getString(1));
                    courseCodeOne.setText(resultSet.getString(2));
                    courseCreditOne.setText(resultSet.getString(3));
                }else if(index == 2){
                   courseNameTwo.setText(resultSet.getString(1));
                   courseCodeTwo.setText(resultSet.getString(2));
                   courseCreditTwo.setText(resultSet.getString(3));
                }else if(index == 3){
                   courseNameThree.setText(resultSet.getString(1));
                   courseCodeThree.setText(resultSet.getString(2));
                   courseCreditThree.setText(resultSet.getString(3));
                }else if(index == 4){
                   courseNameFour.setText(resultSet.getString(1));
                   courseCodeFour.setText(resultSet.getString(2));
                   courseCreditFour.setText(resultSet.getString(3));
                }else if (index == 5) {
                    courseNamePanel5.setVisible(true);
                    courseCreditPanel5.setVisible(true);
                    courseCodePanel5.setVisible(true);
                    
                   courseNameFive.setText(resultSet.getString(1));
                   courseCodeFive.setText(resultSet.getString(2));
                   courseCreditFive.setText(resultSet.getString(3));

                } else if (index == 6) {
                    courseNamePanel6.setVisible(true);
                    courseCodePanel6.setVisible(true);
                    courseCreditPanel6.setVisible(true);
                    
                    courseNameSix.setText(resultSet.getString(1));
                    courseCodeSix.setText(resultSet.getString(2));
                    courseCreditSix.setText(resultSet.getString(3));

                } else if (index == 7) {
                    courseNamePanel7.setVisible(true);
                    courseCodePanel7.setVisible(true);
                    courseCreditPanel7.setVisible(true);
                    
                    courseNameSeven.setText(resultSet.getString(1));
                    courseCodeSeven.setText(resultSet.getString(2));
                    courseCreditSeven.setText(resultSet.getString(3));
                }
                index++;
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

