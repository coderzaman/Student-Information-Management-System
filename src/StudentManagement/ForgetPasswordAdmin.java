    private void backBtnFromDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_backBtnFromDeleteMouseClicked
        if (!checkReadyForDatabese) {
            mainPage.setVisible(true);
            this.dispose();
        } else {
            try {
                connection.close();
            } catch (SQLException ex) {
                Logger.getLogger(AddStudent.class.getName()).log(Level.SEVERE, null, ex);
            }
            checkReadyForDatabese = false;
            mainPage.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_backBtnFromDeleteMouseClicked

    private void forgetPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_forgetPasswordActionPerformed
        // TODO add your handling code here:
        String userNameVar = passwordUserName.getText();
        String userPhoneVar = passwordPhoneName.getText();
        String userNewPass = newPassword.getText();

        if (userNameVar.equals("") || userNameVar.equals("   Enter your username")) {
            JOptionPane.showMessageDialog(rootPane, "Username or ID is requred");
        } else if (userPhoneVar.equals("") || userPhoneVar.equals("   Enter your phone number")) {
            JOptionPane.showMessageDialog(rootPane, "Phone number is requred");
        } else if (userNewPass.equals("") || userNewPass.equals("   Enter your new password")) {
            JOptionPane.showMessageDialog(rootPane, "New passowrd is requred");
        } else {
            try {
                // Connect to the database
                String user = "root";
                String password = "";
                String url = "jdbc:mysql://localhost:3306/studentmangementsystem?serverTimezone=UTC";
                connection = DriverManager.getConnection(url, user, password);
              
                if (mainPage.getSelectedItem().equals("Admin")) {
                    String queryForSelect = "SELECT user_name, phoneNumber FROM admin WHERE user_name = ?";
                    PreparedStatement statementForSelect = connection.prepareStatement(queryForSelect);
                    statementForSelect.setString(1, userNameVar);
                    ResultSet resultSet = statementForSelect.executeQuery();

                    if (resultSet.next()) {
                        String userFromDb = resultSet.getString("user_name");
                        String phoneFromDb = resultSet.getString("phoneNumber");

                        if (userFromDb.equals(userNameVar) && phoneFromDb.equals(userPhoneVar)) {
                            String updatePassword = "UPDATE admin SET password = ? WHERE user_name = ?";
                            PreparedStatement statementForUpadate = connection.prepareStatement(updatePassword);
                            statementForUpadate.setString(1, userNewPass);
                            statementForUpadate.setString(2, userFromDb);

                            int rowEffected = statementForUpadate.executeUpdate();

                            if (rowEffected > 0) {
                                JOptionPane.showMessageDialog(rootPane, "Sucefully change password");
                            }

                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Username and PhoneNumber Not Match");
                        }

                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Username Not Found");
                    }
                } else {
                    String queryForSelect = "SELECT stdId, stdPhone FROM stdinfo WHERE stdId = ?";
                    PreparedStatement statementForSelect = connection.prepareStatement(queryForSelect);
                    statementForSelect.setString(1, userNameVar);
                    ResultSet resultSet = statementForSelect.executeQuery();

                    if (resultSet.next()) {
                        String userFromDb = resultSet.getString("stdId");
                        String phoneFromDb = resultSet.getString("stdPhone");

                        if (userFromDb.equals(userNameVar) && phoneFromDb.equals(userPhoneVar)) {
                            String updatePassword = "UPDATE stdinfo SET stdPasswor = ? WHERE stdId = ?";
                            PreparedStatement statementForUpadate = connection.prepareStatement(updatePassword);
                            statementForUpadate.setString(1, userNewPass);
                            statementForUpadate.setString(2, userFromDb);

                            int rowEffected = statementForUpadate.executeUpdate();

                            if (rowEffected > 0) {
                                JOptionPane.showMessageDialog(rootPane, "Sucefully change password");
                            }

                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Username and PhoneNumber Not Match");
                        }

                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Username Not Found");
                    }
                }

            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

        passwordUserName.setText("   Enter your username");
        passwordUserName.setForeground(Color.gray);
        passwordPhoneName.setText("   Enter your phone number");
        passwordPhoneName.setForeground(Color.gray);
        newPassword.setText("   Enter your new password");
        newPassword.setForeground(Color.gray);
        newPassword.setEchoChar('\u0000');
    }//GEN-LAST:event_forgetPasswordActionPerformed

