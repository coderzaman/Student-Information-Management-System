    private void logInBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInBtnActionPerformed
        //boolean connectWithDb = false;

        if (logInUserName.getText().equals("   Enter your username") || logInUserName.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "User name is requried");
        } else if (logInPassword.getText().equals("   Enter your password") || logInPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Password is requried");
        } else if (selectStdOrAdmin.getSelectedItem().toString().equals("none")) {
            JOptionPane.showMessageDialog(rootPane, "Select user or admin");
        } else {
            String url = "jdbc:mysql://127.0.0.1/studentmangementsystem";
            String user = "root";
            String password = "";

            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                connection = DriverManager.getConnection(url, user, password);

                String selectedOption = (String) selectStdOrAdmin.getSelectedItem();

                if (selectedOption.equals("Admin")) {
                    String loguUername = logInUserName.getText().trim();
                    String logPassword = logInPassword.getText();

                    // Create a SQL query to check if the username and password match
                    String query = "SELECT * FROM admin WHERE user_name = ? AND password = ?";
                    PreparedStatement preparedStatement = connection.prepareStatement(query);
                    preparedStatement.setString(1, loguUername);
                    preparedStatement.setString(2, logPassword);

                    ResultSet resultSetLogIn = preparedStatement.executeQuery();
                    if (resultSetLogIn.next()) {
                        AdminMain adminMain = new AdminMain();
                        adminMain.setVisible(true);
                        this.dispose();
                        connection.close();
                    } else {
                        JOptionPane.showMessageDialog(this, "Please Input Correct Username and Password", "Don't Match", JOptionPane.INFORMATION_MESSAGE);
                    }
                }else if (selectedOption.equals("Student")) {
                    String loguUername = logInUserName.getText().trim();
                    String logPassword = logInPassword.getText();

                    // Create a SQL query to check if the username and password match
                    String query = "SELECT * FROM stdinfo WHERE stdId = ? AND stdPasswor = ?";
                    PreparedStatement preparedStatement = connection.prepareStatement(query);
                    preparedStatement.setString(1, loguUername);
                    preparedStatement.setString(2, logPassword);

                    ResultSet resultSetLogIn = preparedStatement.executeQuery();
                    if (resultSetLogIn.next()) {
                        passStudentId = resultSetLogIn.getString("stdId");
                        StudentHome studentHome = new StudentHome();
                        studentHome.setVisible(true);
                        this.dispose();
                        connection.close();
                    } else {
                        JOptionPane.showMessageDialog(this, "Please Input Correct ID and Password", "Don't Match", JOptionPane.INFORMATION_MESSAGE);
                    }
                }

            } catch (SQLException ex) {
                Logger.getLogger(AddStudent.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_logInBtnActionPerformed

